              Tabira é um município brasileiro do estado de Pernambuco. Administrativamente, a cidade é formada apenas pelo distrito sede e pelos povoados de Brejinho, Borborema, Riacho do Gado e Campos Novos. Sua população foi estimada em 27.681 habitantes, conforme dados do censo do IBGE de 2022, sendo o 4º município mais populoso da Microrregião do Pajeú. <br>

              <br>Em 1865, Tabira era uma fazenda de propriedade do Sr. Gonçalo Gomes dos Santos, que por iniciativa própria, formou uma pequena feira, com o objetivo de atender os moradores da região. O sucesso alcançado foi tamanho que deu início a formação de uma povoação, recebendo inicialmente o nome de Madeira, depois Toco do Gonçalo, em virtude de haver no meio da feira um toco que servia ao talho da carne para venda ao público, posteriormente Espírito Santo ficando até 1939 quando passou a denominar-se Tabira, Em homenagem ao grande guerreiro indígena Tabira, que segundo a lenda, em um combate, foi atingido por uma flecha no olho, e retirando-a com bravura, continuou lutando até vencer seus inimigos.
              Tabira foi desmembrada do município de Afogados da Ingazeira em 31 de dezembro de 1948. Porém só foi constituído município autônomo pela lei n. ° 508, de 27 de maio de 1949, data em que se comemora a sua emancipação política e que coincide com a instalação da primeira Constituição Municipal de Tabira.<br><br>

                Formação Administrativa: <br><br>
