                Assim permanecendo em divisões territoriais datadas de 31-XII-1936 e 31-XII-1937.
                Pelo decreto-lei estadual nº 92, de 31-03-1938, o distrito de Espirito Santo passou a denominar-se Tabira.
                Elevado à categoria de município com a denominação de Tabira, pela lei estadual nº 418, de 31-12-1948, desmembrado de Afogados da Ingazeira. Sede no antigo distrito de Tabira. Constituído de 4 distritos: Tabira, Ingazeira, Solidão e Tuparetama ex-Tupã. Desmembrado de Afogados da Ingazeira Até a data de hoje não temos a sua instalação.
